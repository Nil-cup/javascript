function suma (){
    let n1 = parseFloat(prompt("Dime un numero"));
    let n2 = parseFloat(prompt("Dime otro numnero"));
    document.write(n1+n2)
}

function resta (){
    let n1 = parseFloat(prompt("Dime un numero"));
    let n2 = parseFloat(prompt("Dime otro numnero"));
    document.write(n1-n2)
}

